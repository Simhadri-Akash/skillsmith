import mongoose from 'mongoose';

const enrollmentSchema = new mongoose.Schema({
  courseId: {
    type: Number,
    required: true
  },
  userId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true
  },
  enrolledAt: {
    type: Date,
    default: Date.now
  },
  status: {
    type: String,
    enum: ['active', 'completed', 'cancelled'],
    default: 'active'
  }
});

export default mongoose.model('Enrollment', enrollmentSchema);